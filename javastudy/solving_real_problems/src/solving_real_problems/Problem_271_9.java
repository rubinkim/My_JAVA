package solving_real_problems;

public class Problem_271_9 {

	public static void main(String[] args) {
		byte a = 15, b = -19, c = 14;
		System.out.printf("%d\n", a);
		System.out.printf("%d\n", ~a);
		System.out.printf("%d\n", c);
		System.out.printf("%d\n", ~c);
		System.out.println();
		System.out.printf("%d, %d, %d\n", a&b, a|b, a^b);
	}
}
